var x = 8.9;  // var simply reflect typical variable concept
console.log(x);
x = true;
console.log(x);
x = [2,6,3,8];
console.log(x);

let z = 'P';  // let is also similar to var, but they have some difference.
console.log(z);
z = "Happy";
console.log(z);


